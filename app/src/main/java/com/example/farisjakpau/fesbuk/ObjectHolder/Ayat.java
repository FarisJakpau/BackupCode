public class Ayat {
    String ayat;
    String surah ;
    String id ;
    String num_surah;

    public String getAyat() {
        return ayat;
    }

    public void setAyat(String ayat) {
        this.ayat = ayat;
    }

    public String getSurah() {
        return surah;
    }

    public void setSurah(String surah) {
        this.surah = surah;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNum_surah() {
        return num_surah;
    }

    public void setNum_surah(String num_surah) {
        this.num_surah = num_surah;
    }

}
